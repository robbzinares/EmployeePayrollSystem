            pst.setString(4, surname);
            pst.setString(5, salary);
            pst.setString(6, rice);
            pst.setString(7, clothing);
            pst.setString(8, phone);
            pst.setString(9, bonus);
            pst.setString(10, others);
            pst.setString(11, net);
            pst.setString(12, createdBy);

            pst.executeUpdate();
