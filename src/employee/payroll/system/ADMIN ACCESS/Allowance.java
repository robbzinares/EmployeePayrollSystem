                    "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            pst = conn.prepareStatement(sql);
            pst.setString(1, createdBy);
            pst.setString(2, empId); // Use the searched employee's ID
